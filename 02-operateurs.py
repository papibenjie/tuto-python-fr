# on peut effectuer toute sortes d'operation sur les variables et les valeur
# les plus courante sont les operations arithmetique (+,-,*,/,//,%)

# le type des variables peut modifier le fonctionnement des operateurs

# ------------------------------------------------------
# sur les integer et les float
a = 10
b = 3

print(a + b)  # 13
print(a - b)  # 7
print(a * b)  # 30
print(a / b)  # 3.333333

# L'operateur '//' sert a faire une division entiere
print(a // b)  # 3

# L'operateur '%' sert a obtenir le reste d'une division entiere
print(a % b)  # 1

# ------------------------------------------------------
# Sur les chaines de charachere
a = "abc"
b = 3

print(a * b)  # "abcabcabc"


# On peut utiliser les parenthese () pour specifier l'ordre des operations
# 2 * 2 - 1
# 3

# 2 * (2 - 1)
# 1

# On peut aussi les utiliser pour rendre le code plus claire et plus lisible
