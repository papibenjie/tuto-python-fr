# les variables sont des contenants dans lesquels on peut stocker des valeurs

a = 15  # a -> 15
a = 10  # a -> 10

# on peut meme print() les variables
print(a)

# les variables peuvent prendre plusieurs type

# integer (entier)
a = 10

# float (decimal)
a = 9.9999
print(a)

# string (chaine de character)
a = "allo"
print(a)

# boolean
a = True
print(a)

# il existe plusieurs autres type..
# plus tard on pourra meme creer nos propres type !


# il est possible de convertir les variable d'un type en d'autre type en utilisant les fonctions correspondante
a = 3.3333  # a -> 3.3333

a = int(a)  # a -> 3

a = str(a)  # a -> "a"

a = bool(a)  # a -> True
